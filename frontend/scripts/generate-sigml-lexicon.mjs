// Generates src/avatar/sigml-lexicon.gen.ts from the human-divanshu
// Text-to-Sign-Language SiGML corpus (Indian Sign Language).
//
// Usage:
//   node scripts/generate-sigml-lexicon.mjs [path/to/SignFiles]
//
// With no argument the source repository is shallow-cloned into a temp
// directory and removed afterwards. Pass a path (the SignFiles directory or
// the repo root) to generate from an existing checkout instead.

import { execFileSync } from "node:child_process"
import {
  existsSync,
  mkdtempSync,
  readdirSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs"
import { tmpdir } from "node:os"
import { dirname, join, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const REPO_URL = "https://github.com/human-divanshu/Text-to-Sign-Language.git"
const scriptDir = dirname(fileURLToPath(import.meta.url))
const outFile = resolve(scriptDir, "../src/avatar/sigml-lexicon.gen.ts")

// Resolve the SignFiles directory: a CLI path argument if given, otherwise a
// fresh shallow clone. Returns the directory plus a cleanup callback.
function resolveSource() {
  const arg = process.argv[2]
  if (arg) {
    let dir = resolve(arg)
    if (!existsSync(dir)) {
      throw new Error(`Path not found: ${dir}`)
    }
    // Accept either the SignFiles directory itself or the repo root.
    if (existsSync(join(dir, "SignFiles"))) {
      dir = join(dir, "SignFiles")
    }
    return { dir, cleanup: () => {} }
  }
  const tmp = mkdtempSync(join(tmpdir(), "t2sl-"))
  console.log(`Cloning ${REPO_URL} ...`)
  execFileSync("git", ["clone", "--depth", "1", REPO_URL, tmp], {
    stdio: "inherit",
  })
  return {
    dir: join(tmp, "SignFiles"),
    cleanup: () => rmSync(tmp, { recursive: true, force: true }),
  }
}

// Turn a *.sigml filename into the UPPERCASE key that tokeniseGlosses()
// produces: drop the extension, drop GitHub's " (Case Conflict)" dedupe
// suffix, then uppercase and strip every character the tokeniser strips.
function fileToKey(filename) {
  return filename
    .replace(/\.sigml$/i, "")
    .replace(/\s*\(case conflict\)\s*/i, "")
    .toUpperCase()
    .replace(/[^A-Z0-9-]/g, "")
}

// Extract every <hns_sign>...</hns_sign> fragment from a SiGML document and
// collapse inter-tag whitespace so the bundled lexicon stays compact. The
// source files are full <sigml> documents; assemble.ts adds that wrapper, so
// only the bare fragments are kept here.
function extractSign(xml) {
  const matches = xml.match(/<hns_sign\b[\s\S]*?<\/hns_sign>/gi)
  if (!matches) {
    return null
  }
  return matches.map((m) => m.replace(/>\s+</g, "><").trim()).join("")
}

function buildLexicon(signFilesDir) {
  // Sort non-conflict files first so they win key collisions against the
  // " (Case Conflict)" duplicates created on case-insensitive checkouts.
  const files = readdirSync(signFilesDir)
    .filter((f) => f.toLowerCase().endsWith(".sigml"))
    .sort((a, b) => {
      const ca = /case conflict/i.test(a)
      const cb = /case conflict/i.test(b)
      if (ca !== cb) {
        return ca ? 1 : -1
      }
      return a.localeCompare(b)
    })

  const lexicon = {}
  const skipped = { empty: 0, noSign: 0, collision: 0 }

  for (const file of files) {
    const xml = readFileSync(join(signFilesDir, file), "utf8")
    if (!xml.trim()) {
      skipped.empty++
      continue
    }
    const fragment = extractSign(xml)
    const key = fileToKey(file)
    if (!fragment || !key) {
      skipped.noSign++
      continue
    }
    if (Object.hasOwn(lexicon, key)) {
      skipped.collision++
      continue
    }
    lexicon[key] = fragment
  }
  return { lexicon, skipped }
}

function render(lexicon) {
  const keys = Object.keys(lexicon).sort()
  const body = keys
    .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(lexicon[k])},`)
    .join("\n")
  return `// AUTO-GENERATED by scripts/generate-sigml-lexicon.mjs — do not edit by hand.
//
// Indian Sign Language (ISL) signs in SiGML/HamNoSys notation.
// Source: https://github.com/human-divanshu/Text-to-Sign-Language (SignFiles/)
// Regenerate with: npm run generate-lexicon
//
// ${keys.length} signs. Each value is a bare <hns_sign> fragment; assemble.ts
// wraps the chosen fragments in <sigml>...</sigml> for the CWASA runtime.

export const SIGML_LEXICON: Record<string, string> = {
${body}
}
`
}

function main() {
  const { dir, cleanup } = resolveSource()
  try {
    const { lexicon, skipped } = buildLexicon(dir)
    writeFileSync(outFile, render(lexicon))
    const count = Object.keys(lexicon).length
    console.log(`Wrote ${count} signs -> ${outFile}`)
    console.log(
      `Skipped: ${skipped.empty} empty, ${skipped.noSign} no-sign, ${skipped.collision} duplicate keys`,
    )
  } finally {
    cleanup()
  }
}

main()
